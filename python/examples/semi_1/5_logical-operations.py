# Алгебра логики(Булевская Алгебра) True(1) False(0)

# конъюнкция(логическое умножение) - and
# дизъюнкция(логическое сложение) - or
# отрицание(обратное значение) - not

print(7 > 5 and 10 < 0)
# #      1  *   0 = 0(False)

print(1 > 2 or 5 > -1)
# #      0    +    1 = 1

print(3 > 2 or 5 > 2)
# #      1    +    1 = 1

print((3 > 2 or 5 > 3) and 2 < 0)